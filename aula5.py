# Tipo de dado bool (boolean)
# Ao questionar algo em um programa.
# Só existem duas respostas possíveis
# sim (true) ou não (false)
# Existem vários operadores para "Questionar".
# Dentre eles o ==, que é um operador lógico que
# questiona se um valor é igual o outro.

# True e False tem a letra inicial maiúscula, nao existe true e false.
print(10 == 10) # Sim -> True

print(10 == 11) # Não -> False

print (type(True)) # Boolean
print (type(False)) # Boolean

# print (type(true)) # Não existe
# print (type(false)) # Não existe

print (type(1 == 1)) # Boolean
print (type(1 == 2)) # Boolean